import math  # Імпорт бібліотеки для математичних функцій

# Введення значення x користувачем
x = float(input("Введіть значення x: "))

# Обчислення виразу залежно від умови
if x > 45:
    z = -math.sqrt(x)  # Обчислення -√x для x > 45
else:
    z = math.sin(x)**2  # Обчислення sin²(x) для x ≤ 45

# Виведення результату
print(f"Значення z: {z}")
